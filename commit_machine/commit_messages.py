class CommitMessageProvider:
    """Serves commit messages according to configured mode."""

    def __init__(self, mode: str = "random", start_index: int = 0) -> None:
        self.mode = mode
        self._index = start_index % len(COMMIT_MESSAGES)
        self._cycle: Iterator[str] = itertools.cycle(COMMIT_MESSAGES)

        # Advance cycle to start_index for sequential mode continuity
        for _ in range(self._index):
            next(self._cycle)

    def next_message(self, *, rng_choice=None) -> str:
        if self.mode == "sequential":
            message = next(self._cycle)
            self._index = (self._index + 1) % len(COMMIT_MESSAGES)
            return message
        if self.mode == "enterprise":
            # Prefer longer, more ceremonial messages
            pool = [m for m in COMMIT_MESSAGES if len(m) > 40]
            if rng_choice is None:
                import random

                return random.choice(pool or COMMIT_MESSAGES)
            return rng_choice(pool or COMMIT_MESSAGES)

        if rng_choice is None:
            import random

            return random.choice(COMMIT_MESSAGES)
        return rng_choice(COMMIT_MESSAGES)

    @property
    def index(self) -> int:
        return self._index
